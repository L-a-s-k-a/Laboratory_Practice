#include "init.h"
void GPIO_Ini_1(void) {
    // 开启GPIO B和GPIO C时钟
    SET_BIT(RCC_GPIO_EN, RCC_GPIOB_EN | RCC_GPIOC_EN);

    // 设置GPIOB的第7、0、14号引脚为输出模式
    SET_BIT(GPIOB_MODER, GPIOB_MODE_PIN7_OUT | GPIOB_MODE_PIN0_OUT | GPIOB_MODE_PIN14_OUT);

    // 设置GPIOB的第7、0、14号引脚为推挽输出
    SET_BIT(GPIOB_OTYPER, GPIOB_OTYPE_PIN7_PP | GPIOB_OTYPE_PIN0_PP | GPIOB_OTYPE_PIN14_PP);

    // 设置GPIOB的第7、0、14号引脚的输出速度为中等速度
    SET_BIT(GPIOB_OSPEEDR, GPIOB_OSPEED_PIN7_MID | GPIOB_OSPEED_PIN0_MID | GPIOB_OSPEED_PIN14_MID);

    // 禁用GPIOB的第7、0、14号引脚的上拉/下拉电阻
    SET_BIT(GPIOB_PUPDR, GPIOB_PUPDR_PIN7_NOPUPD | GPIOB_PUPDR_PIN0_NOPUPD | GPIOB_PUPDR_PIN14_NOPUPD);
}

