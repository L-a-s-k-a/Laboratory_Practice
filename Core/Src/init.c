void GPIO_Ini_Self_Def(void)
{
    SET_BIT_SELF(RCC_AAB1ENR, RCC_GPIOBEN | RCC_GPIOCEN);
    SET_BIT_SELF(GPIO_MODER, GPIO_PIN_7_OUT);
    SET_BIT_SELF(GPIO_OSPEEDR, GPIO_PIN_7_MED);
    SET_BIT_SELF(GPIO_BSRR, GPIO_PIN_7_RESET);
}
void GPIO_Ini_CMSIS(void)
{
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE14_0); // GPIOB MODE 14 -> OUTPUT
    CLEAR_BIT(GPIOB->OTYPER, GPIO_OTYPER_OT14);
    SET_BIT(GPIOB->OSPEEDR, GPIO_OSPEEDER_OSPEEDR14_0);
    SET_BIT(GPIOB->BSRR, GPIO_BSRR_BR14);
}
