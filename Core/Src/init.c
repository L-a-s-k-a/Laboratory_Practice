void GPIO_Ini(void){
    
    GPIOC_MODER |= GPIOC_MODE_OUT_PIN6; 
    GPIOC_OTYPER |= GPIOC_TYPE_PP_PIN6;
    GPIOC_OSPEEDR |= GPIOC_SPEED_M_PIN6;
    GPIOC_PUPDR |= GPIOC_PUPDR_NPUPD_PIN6;

    
    SET_BIT(RCC->AHB1ENR, RCC_AHB1ENR_GPIOBEN | RCC_AHB1ENR_GPIOCEN | RCC_AHB1ENR_GPIODEN); 

    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE0_0);  //Вторая лаба
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE7_0);  //Вторая лаба
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE14_0); //Вторая лаба
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE5_0); //Вторая лаба
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE13_0); //Вторая лаба
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE14_0); //Вторая лаба
    SET_BIT(GPIOB->MODER, GPIO_MODER_MODE15_0); //Вторая лаба
    SET_BIT(GPIOD->MODER, GPIO_MODER_MODE6_0); //Вторая лаба
    SET_BIT(GPIOD->MODER, GPIO_MODER_MODE7_0); //Вторая лаба

    SET_BIT(GPIOD->MODER, GPIO_MODER_MODE6_0);  
    SET_BIT(GPIOD->MODER, GPIO_MODER_MODE7_0); 

    CLEAR_BIT(GPIOB->OTYPER, GPIO_OTYPER_OT_7 | GPIO_OTYPER_OT_0 | GPIO_OTYPER_OT_14 | GPIO_OTYPER_OT_4 | GPIO_OTYPER_OT_15); 
    SET_BIT(GPIOB->OSPEEDR, GPIO_OSPEEDER_OSPEEDR7_0 | GPIO_OSPEEDER_OSPEEDR14_0 | GPIO_OSPEEDER_OSPEEDR0_0 | GPIO_OSPEEDER_OSPEEDR4_0 | GPIO_OSPEEDER_OSPEEDR15_0); 
    CLEAR_BIT(GPIOB->PUPDR, GPIO_PUPDR_PUPDR7_0 | GPIO_PUPDR_PUPDR14_0 | GPIO_PUPDR_PUPDR0_0 | GPIO_PUPDR_PUPDR15_0); 

    CLEAR_BIT(GPIOD->OTYPER, GPIO_OTYPER_OT_7 | GPIO_OTYPER_OT_6 ); 
    SET_BIT(GPIOD->OSPEEDR, GPIO_OSPEEDER_OSPEEDR7_0 | GPIO_OSPEEDER_OSPEEDR6_0); 
    CLEAR_BIT(GPIOD->PUPDR, GPIO_PUPDR_PUPDR7_0 | GPIO_PUPDR_PUPDR6_0);
}

void RCC_Init(void)
{
    MODIFY_REG(RCC->CR, RCC_CR_HSITRIM, 0x80U);
    CLEAR_REG(RCC->CFGR);
    while (READ_BIT(RCC->CFGR, RCC_CFGR_SWS) != RESET)
        ;
    CLEAR_BIT(RCC->CR, RCC_CR_PLLON);
    while (READ_BIT(RCC->CR, RCC_CR_PLLRDY) != RESET)
        ;
    CLEAR_BIT(RCC->CR, RCC_CR_HSEON | RCC_CR_CSSON);
    while (READ_BIT(RCC->CR, RCC_CR_HSERDY) != RESET)
        ;
    CLEAR_BIT(RCC->CR, RCC_CR_HSEBYP);
    SET_BIT(RCC->CR, RCC_CR_HSEON); 
    while (READ_BIT(RCC->CR, RCC_CR_HSERDY) == RESET)
        ;
    CLEAR_BIT(RCC->CR, RCC_CR_HSEBYP);       
    SET_BIT(RCC->CR, RCC_CR_CSSON);           
                                               
    SET_BIT(RCC->PLLCFGR, RCC_PLLCFGR_PLLP_0); 
    SET_BIT(RCC->PLLCFGR, RCC_PLLCFGR_PLLSRC_HSE);
    MODIFY_REG(RCC->PLLCFGR, RCC_PLLCFGR_PLLM, RCC_PLLCFGR_PLLM_2);
    MODIFY_REG(RCC->PLLCFGR, RCC_PLLCFGR_PLLN_Msk, RCC_PLLCFGR_PLLN_2 | RCC_PLLCFGR_PLLN_4 | RCC_PLLCFGR_PLLN_5 | RCC_PLLCFGR_PLLN_7);
    CLEAR_BIT(RCC->PLLCFGR, RCC_PLLCFGR_PLLP_Msk);
    SET_BIT(RCC->CR, RCC_CR_PLLON); 
    while (READ_BIT(RCC->CR, RCC_CR_PLLRDY))
        ;

    MODIFY_REG(RCC->CFGR, RCC_CFGR_SW, RCC_CFGR_SW_PLL);        
    MODIFY_REG(RCC->CFGR, RCC_CFGR_HPRE, RCC_CFGR_HPRE_DIV1);    
    MODIFY_REG(RCC->CFGR, RCC_CFGR_PPRE1, RCC_CFGR_PPRE1_DIV4);   
    MODIFY_REG(RCC->CFGR, RCC_CFGR_PPRE2, RCC_CFGR_PPRE2_DIV2);    
    MODIFY_REG(RCC->CFGR, RCC_CFGR_MCO2PRE, RCC_CFGR_MCO2PRE_Msk);
    CLEAR_BIT(RCC->CFGR, RCC_CFGR_MCO2);                          

    MODIFY_REG(FLASH->ACR, FLASH_ACR_LATENCY, FLASH_ACR_LATENCY_5WS);
}

void ITR_init(void)
{
    SET_BIT(RCC->APB2ENR, RCC_APB2ENR_SYSCFGEN);                                           
    MODIFY_REG(SYSCFG->EXTICR[1], SYSCFG_EXTICR2_EXTI4_Msk, SYSCFG_EXTICR2_EXTI4_PB);      
    SET_BIT(EXTI->IMR, EXTI_IMR_MR4);                                                      
    SET_BIT(EXTI->RTSR, EXTI_RTSR_TR4);                                                   
    SET_BIT(EXTI->FTSR, EXTI_FTSR_TR4);                                                     
    NVIC_SetPriority(EXTI4_IRQn, NVIC_EncodePriority(NVIC_GetPriorityGrouping(), 0, 0)); 
    NVIC_EnableIRQ(EXTI4_IRQn);                                                        

}

void SysTick_Init(void)
{
    CLEAR_BIT(SysTick->CTRL, SysTick_CTRL_ENABLE_Msk);                                    
    SET_BIT(SysTick->CTRL, SysTick_CTRL_TICKINT_Msk);                                    
    SET_BIT(SysTick->CTRL, SysTick_CTRL_CLKSOURCE_Msk);                                   
    MODIFY_REG(SysTick->LOAD, SysTick_LOAD_RELOAD_Msk, 179999 << SysTick_LOAD_RELOAD_Pos); 
    MODIFY_REG(SysTick->VAL, SysTick_VAL_CURRENT_Msk, 179999 << SysTick_VAL_CURRENT_Pos);  
    SET_BIT(SysTick->CTRL, SysTick_CTRL_ENABLE_Msk);                                      
}